public class Main {
    public static void main(String[] args) {
        // Английский алфавит
        for(char i = 'a'; i <= 'z'; i++){
            int c = i;
            System.out.println(i + " : " + c + ".");
        }
        for(char i = 'A'; i <= 'Z'; i++){
            int d = i;
            System.out.println(i + " : " + d + ".");
        }

        // Английский алфавит
        for(char i = 'а'; i <= 'я'; i++){
            int f = i;
            System.out.println(i + " : " + f + ".");
        }
        for(char i = 'А'; i <= 'Я'; i++){
            int g = i;
            System.out.println(i + " : " + g + ".");
        }
    }
}
